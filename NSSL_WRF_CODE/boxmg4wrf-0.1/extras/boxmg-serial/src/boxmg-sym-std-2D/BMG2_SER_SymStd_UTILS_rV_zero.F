      SUBROUTINE BMG2_SER_SymStd_UTILS_rV_zero( v, Nx, Ny )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     Sets a vector, v, of dimensions Nx by Ny to zero.
C     
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include       "BMG_SER_constants.h"

C ----------------------------
C     Argument Declarations
C 
      INTEGER Nx, Ny
      REAL*RKIND  v(Nx*Ny)

C ----------------------------
C     Local Declarations
C
      INTEGER i

C =========================================================================

      DO i=1, Nx*Ny
         v(i) = rZERO
      END DO

C =========================================================================

      RETURN
      END

