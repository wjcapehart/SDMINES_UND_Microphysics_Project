      SUBROUTINE BMG2_SER_SymStd_UTILS_norm_l2( u, Nx, Ny, l2norm )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG2_SER_SymStd_norm_l2.f computes the l2-norm of a grid function
C     (the vector u).  It assumes that ghost points should be neglected.
C
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include       "BMG_SER_constants.h"

C ----------------------------
C     Argument Declarations
C 
      INTEGER  Nx, Ny
      REAL*RKIND   l2norm, u(Nx,Ny)

C ----------------------------
C     Local Declarations
C
      INTEGER  i, j

C =========================================================================

      l2norm=rZERO

      DO j = 2, Ny-1
         DO i = 2, Nx-1
            l2norm=l2norm+u(i,j)*u(i,j)
         END DO
      END DO

      l2norm=SQRT(l2norm)

C =========================================================================

      RETURN
      END
