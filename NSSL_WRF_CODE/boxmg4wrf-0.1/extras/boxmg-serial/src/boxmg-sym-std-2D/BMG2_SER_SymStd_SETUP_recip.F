      SUBROUTINE BMG2_SER_SymStd_SETUP_recip( 
     &                SO, SOR, Nx, Ny, NStncl, NSOR_v
     &                )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG2_SER_SymStd_cons_recip.f computes the reciprocal of the central
C     stencil coefficient.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include       "BMG_SER_constants.h"
      INCLUDE 'BMG_SER_stencils.h'

C ---------------------------
C    Argument Declarations:
C
      INTEGER   NSOR_v, NStncl, Nx, Ny
      REAL*RKIND    SO(Nx,Ny,NStncl), SOR(Nx,Ny,NSOR_v)

C --------------------------
C     Local Declarations:
C
      INTEGER   i, j

C ==========================================================================

      DO j = 2, Ny-1
         DO i = 2, Nx-1
            SOR(i,j,msor)=rONE/SO(i,j,ko)
         ENDDO
      ENDDO

C ==========================================================================

C =======================

         RETURN
         END
