      SUBROUTINE BMG2_SER_SymStd_UTILS_dcopy( x, y, Nx, Ny )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG2_SER_SymStd_dcopy.f replaces y with x.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include       "BMG_SER_constants.h"

C ----------------------------
C     Argument Declarations
C 
      INTEGER  Nx, Ny
      REAL*RKIND   x(Nx,Ny), y(Nx,Ny)

C ----------------------------
C     Local Declarations
C
      INTEGER  i, j

C =========================================================================

      DO j = 2, Ny-1
         DO i = 2, Nx-1
            y(i,j)=x(i,j)
c            print *, i,j,y(i,j)
         END DO
      END DO

C =========================================================================

      RETURN
      END
