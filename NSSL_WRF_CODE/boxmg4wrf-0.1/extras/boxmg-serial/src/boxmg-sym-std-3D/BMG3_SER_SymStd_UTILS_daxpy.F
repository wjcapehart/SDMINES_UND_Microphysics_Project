      SUBROUTINE BMG3_SER_SymStd_UTILS_daxpy( alpha, x, y, Nx, Ny, Nz)

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C   BMG3_SER_SymStd_daxpy.f replaces y with a*x + y.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include       "BMG_SER_constants.h"

C ----------------------------
C     Argument Declarations
C 
      INTEGER  Nx, Ny, Nz
      REAL*RKIND   alpha, x(Nx,Ny,Nz), y(Nx,Ny,Nz)

C ----------------------------
C     Local Declarations
C
      INTEGER  i, j, k

C =========================================================================

      DO k = 2, Nz-1
         DO j = 2, Ny-1
            DO i = 2, Nx-1
               y(i,j,k) = y(i,j,k) + alpha*x(i,j,k)
            END DO
         END DO
      END DO


C =========================================================================

      RETURN
      END
