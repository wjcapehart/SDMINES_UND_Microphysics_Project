      subroutine vecprod (prod,u,v,L,M)
      
C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     Compute the inner product of two vectors.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      implicit none

      integer L,M
      real*8 u(0:L+1,0:M+1), v(0:L+1,0:M+1), prod

      integer i,j
   
C ==========================================================================

      prod = 0.0
      do j=1,M
         do i=1,L
            prod = prod+u(i,j)*v(i,j)
         enddo
      enddo

C ==========================================================================

      return
      end
