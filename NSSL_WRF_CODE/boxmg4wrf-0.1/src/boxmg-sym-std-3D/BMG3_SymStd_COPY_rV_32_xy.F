      SUBROUTINE BMG3_SymStd_COPY_rV_32_xy( 
     &                       Q, Q2, iPL, Nx, Ny, Nz 
     &                       )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG3_SymStd_COPY_rV_32_xy copies the a REAL 2D vector to the
C     iPL{th}-(x,y) plane of a REAL 3D vector.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C ---------------------------
C     Argument Declarations:
C
      INTEGER  iPL, Nx, Ny, Nz
      REAL*RKIND   Q(Nx,Ny,Nz), Q2(Nx,Ny)

C ---------------------------
C     Local Declarations:
C
      INTEGER  i, j

C ==========================================================================

      DO j=2, Ny-1
         DO i=2, Nx-1
            Q2(i,j) = Q(i,j,iPL)
         ENDDO
      ENDDO

C ==========================================================================

      RETURN
      END

