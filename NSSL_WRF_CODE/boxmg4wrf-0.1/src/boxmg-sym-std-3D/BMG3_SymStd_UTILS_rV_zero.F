      SUBROUTINE BMG3_SymStd_UTILS_rV_zero( kg, q, Nx, Ny, Nz )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     Sets a vector, v, of dimensions Nx by Ny by Nz to zero.
C     
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include    "BMG_constants.h"

C ----------------------------
C     Argument Declarations
C 
      INTEGER kg, Nx, Ny, Nz
      REAL*RKIND q(Nx*Ny*Nz)

C ----------------------------
C     Local Declarations
C
      INTEGER i

C =========================================================================

      DO i=1, Nx*Ny*Nz
         q(i)= rZERO
      ENDDO

C =========================================================================

      RETURN
      END
