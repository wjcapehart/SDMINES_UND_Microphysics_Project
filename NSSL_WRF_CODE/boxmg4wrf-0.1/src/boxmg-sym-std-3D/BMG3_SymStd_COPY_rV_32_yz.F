      SUBROUTINE BMG3_SymStd_COPY_rV_32_yz( 
     &                       Q, Q2, iPL, Nx, Ny, Nz 
     &                       )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG3_SymStd_COPY_rV_32_yz copies the iPL{th}-(y,z) plane of 
C     a REAL 3D vector to a REAL 2D vector.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT   NONE

C ---------------------------
C     Argument Declarations:
C
      INTEGER  iPL, Nx, Ny, Nz
      REAL*RKIND   Q(Nx,Ny,Nz), Q2(Ny,Nz)

C ---------------------------
C     Local Declarations:
C
      INTEGER  j, k

C ==========================================================================

      DO k=2, Nz-1
         DO j=2, Ny-1
            Q2(j,k) = Q(iPL,j,k)
         ENDDO
      ENDDO

C ==========================================================================

      RETURN
      END

