      SUBROUTINE BMG3_SymStd_COPY_rV_32_xz( 
     &                       Q, Q2, iPL, Nx, Ny, Nz 
     &                       )

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG3_SymStd_COPY_rV_32_xz copies the iPL{th}-(x,z) plane of 
C     a REAL 3D vector to a REAL 2D vector.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C ---------------------------
C     Argument Declarations:
C
      INTEGER  iPL, Nx, Ny, Nz
      REAL*RKIND   Q(Nx,Ny,Nz), Q2(Nx,Nz)

C ---------------------------
C     Local Declarations:
C
      INTEGER  i, k

C ==========================================================================

      DO k=2, Nz-1
         DO i=2, Nx-1
            Q2(i,k) = Q(i,iPL,k)
         ENDDO
      ENDDO

C ==========================================================================

      RETURN
      END

