      SUBROUTINE BMG2_SymStd_UTILS_dscal( alpha, x, Nx, Ny)

C ==========================================================================
C  --------------------
C   DESCRIPTION:
C  --------------------
C
C     BMG2_SymStd_dscal.f replaces x with a*x.
C
C =======================================================================
C $license_flag$
C =======================================================================
C  --------------------
C   INPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   INPUT/OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   OUTPUT:
C  --------------------
C
C
C =======================================================================
C  --------------------
C   LOCAL:
C  --------------------
C
C
C ==========================================================================

      IMPLICIT NONE

C -----------------------------
C     Includes
C
#include      "BMG_constants.h"

C ----------------------------
C     Argument Declarations
C 
      INTEGER  Nx, Ny
      REAL*RKIND   alpha, x(Nx,Ny)

C ----------------------------
C     Local Declarations
C
      INTEGER  i, j

C ==========================================================================

      DO j = 2, Ny-1
         DO i = 2, Nx-1
            x(i,j)=alpha*x(i,j)
         END DO
      END DO


C ==========================================================================

      RETURN
      END
